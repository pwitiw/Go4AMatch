public class Teams {
    private String home;
    private String away;
    private String formHome;
    private String formAway;

    public String getHome() {
        return home;
    }

    public void setHome(String home) {
        this.home = home;
    }

    public String getAway() {
        return away;
    }

    public void setAway(String away) {
        this.away = away;
    }

    public String getFormHome() {
        return formHome;
    }

    public void setFormHome(String formHome) {
        this.formHome = formHome;
    }

    public String getFormAway() {
        return formAway;
    }

    public void setFormAway(String formAway) {
        this.formAway = formAway;
    }
}